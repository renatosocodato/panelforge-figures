def scan_consensus_cache(cache_dir: Path) -> dict[str, list[dict[str, Any]]]:
    """Walk a Consensus cache directory.

    Each ``*.json`` file is read tolerantly:

    * the ``query`` field (if present) keys the result; falls back to
      the file stem otherwise;
    * either ``papers`` or ``top_papers`` (some E9 snapshots use the
      legacy field name) populates the value list;
    * malformed JSON / unreadable files are silently skipped so a
      single corrupt entry doesn't abort the whole scan.

    Returns
    -------
    dict[str, list[dict]]
        Mapping ``query → paper records``. Each record carries the
        Consensus fields (``title``, ``authors``, ``year``,
        ``journal``, ``doi``, ``abstract``, ``relevance_score``) —
        consumers should treat any field as optional.
    """
    if not cache_dir.exists() or not cache_dir.is_dir():
        return {}
    results: dict[str, list[dict[str, Any]]] = {}
    for cache_file in sorted(cache_dir.glob("*.json")):
        try:
            data = json.loads(cache_file.read_text(encoding="utf-8"))
        except (json.JSONDecodeError, OSError, UnicodeDecodeError):
            continue
        if not isinstance(data, dict):
            continue
        query = data.get("query") or cache_file.stem
        papers = data.get("papers") or data.get("top_papers") or []
        if not isinstance(papers, list):
            continue
        valid = [p for p in papers if isinstance(p, dict)]
        if valid:
            results[str(query)] = valid
    return results


