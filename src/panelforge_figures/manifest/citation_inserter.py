"""Elevation 14 — smart citation insertion from Consensus cache.

E9-phase-1 (``novelty_scout``) already calls the Consensus.app Pro API and
caches the top-paper records per claim/query under
``panelforge_workspace/.consensus_cache/``. E14 closes the loop: it walks
the cache, pairs each cached query with a manuscript claim sentence, and
**suggests ``\\cite{...}`` insertions** at sentence-end positions where
the claim needs literature backing. The module also auto-fills
``references.bib`` with new BibTeX entries derived from the cached papers
— it never modifies existing prose, only appends a citation marker just
before the terminal punctuation of each targeted sentence.

Workflow
--------
1. Parse the manuscript via :func:`manuscript_parse.parse_manuscript` (E10).
2. Walk ``existing.claims`` — every "Figure N shows X" sentence is a
   candidate insertion site.
3. Scan the on-disk Consensus cache via :func:`scan_consensus_cache`.
4. For each claim sentence, find the cached query record whose stored
   ``query`` text has the highest TF-IDF cosine similarity to the claim
   sentence; if it clears ``min_similarity``, emit a
   :class:`CitationSuggestion` for the top-3 papers in that record.
5. ``apply_citation_insertions`` writes the actual ``\\cite{...}`` markers
   in-place (backing up the original) and appends new BibTeX entries to
   ``references.bib`` (also backed up).

Public surface
--------------
* Dataclasses: :class:`CitationSuggestion`, :class:`BibEntry`,
  :class:`CitationInsertionResult`.
* Error: :class:`InserterError`.
* Pure helpers: :func:`scan_consensus_cache`,
  :func:`build_bib_entries_from_consensus`.
* Pipeline: :func:`suggest_citations_for_manuscript`,
  :func:`apply_citation_insertions`.
* Reporter: :func:`render_suggestions_markdown`.
"""

