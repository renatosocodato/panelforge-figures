def _backup_path(path: Path) -> Path:
    """Return ``<path>.bak`` (or ``<path>.bak.<n>`` if that exists).

    Never overwrites an existing backup — a fresh backup is created on
    every apply run so the user can roll back to any prior state.
    """
    candidate = path.with_suffix(path.suffix + ".bak")
    if not candidate.exists():
        return candidate
    i = 1
    while True:
        candidate = path.with_suffix(path.suffix + f".bak.{i}")
        if not candidate.exists():
            return candidate
        i += 1


def apply_citation_insertions(
    manuscript_path: Path,
    suggestions: list[CitationSuggestion],
    new_bib_entries: list[BibEntry],
    *,
    existing_bib_path: Path | None = None,
    backup: bool = True,
) -> tuple[Path, Path]:
    """Apply :class:`CitationSuggestion` insertions to disk.

    Behaviour
    ---------
    * Backs up ``manuscript_path`` to ``<path>.bak.tex`` /
      ``<path>.bak.md`` before any edit (when ``backup=True``).
    * Sorts suggestions by ``(line_number desc, char_offset desc)``
      so earlier offsets stay valid as later ones are mutated.
    * Inserts ``" \\cite{key1,key2,...}"`` immediately before the
      terminal punctuation of the targeted sentence.
    * Appends new BibTeX entries to ``existing_bib_path`` (defaults to
      ``manuscript_path.parent / "references.bib"``). Skips entries
      whose cite key already appears in the file.

    Parameters
    ----------
    manuscript_path
        File to mutate.
    suggestions
        From :func:`suggest_citations_for_manuscript`.
    new_bib_entries
        From :func:`suggest_citations_for_manuscript`.
    existing_bib_path
        ``references.bib`` location. Created if missing.
    backup
        When ``False``, edits the manuscript and bib in-place without
        keeping a ``.bak`` copy. Default ``True`` is strongly
        recommended.

    Returns
    -------
    tuple[Path, Path]
        ``(manuscript_path, bib_path)`` for caller convenience.

    Raises
    ------
    InserterError
        On unreadable input, write failure, or invalid line/offset.
    """
    bib_path = existing_bib_path or (manuscript_path.parent / "references.bib")

    # Read manuscript.
    try:
        raw = manuscript_path.read_text(encoding="utf-8")
    except (OSError, UnicodeDecodeError) as exc:
        raise InserterError(f"cannot read manuscript: {exc}") from exc

    # Preserve trailing newline if present.
    trailing_newline = raw.endswith("\n")
    lines = raw.splitlines()

    # Backup manuscript before mutating.
    if backup and suggestions:
        try:
            _backup_path(manuscript_path).write_text(raw, encoding="utf-8")
        except OSError as exc:
            raise InserterError(f"cannot back up manuscript: {exc}") from exc

    # Apply edits in reverse order so earlier offsets stay valid.
    sorted_sugs = sorted(
        suggestions,
        key=lambda s: (s.line_number, s.char_offset),
        reverse=True,
    )
    for sug in sorted_sugs:
        idx = sug.line_number - 1
        if not (0 <= idx < len(lines)):
            raise InserterError(
                f"suggestion line {sug.line_number} out of bounds "
                f"(manuscript has {len(lines)} lines)"
            )
        line = lines[idx]
        # Clamp offset to the line length to survive trailing
        # whitespace differences between dry-run and apply.
        offset = max(0, min(sug.char_offset, len(line)))
        if not sug.cite_keys:
            continue
        cite_marker = f" \\cite{{{','.join(sug.cite_keys)}}}"
        lines[idx] = line[:offset] + cite_marker + line[offset:]

    new_text = "\n".join(lines)
    if trailing_newline:
        new_text += "\n"
    try:
        manuscript_path.write_text(new_text, encoding="utf-8")
    except OSError as exc:
        raise InserterError(f"cannot write manuscript: {exc}") from exc

    # Augment references.bib.
    existing_keys = _extract_existing_cite_keys(bib_path)
    fresh_entries = [e for e in new_bib_entries if e.cite_key not in existing_keys]
    if fresh_entries:
        if bib_path.exists():
            try:
                old_bib = bib_path.read_text(encoding="utf-8")
            except (OSError, UnicodeDecodeError) as exc:
                raise InserterError(f"cannot read existing bib: {exc}") from exc
            if backup:
                try:
                    _backup_path(bib_path).write_text(old_bib, encoding="utf-8")
                except OSError as exc:
                    raise InserterError(f"cannot back up bib: {exc}") from exc
        else:
            old_bib = ""
        sep = "\n" if old_bib and not old_bib.endswith("\n") else ""
        block = "\n".join(e.to_bibtex() for e in fresh_entries)
        if not block.endswith("\n"):
            block += "\n"
        try:
            bib_path.write_text(old_bib + sep + block, encoding="utf-8")
        except OSError as exc:
            raise InserterError(f"cannot write bib: {exc}") from exc

    return manuscript_path, bib_path


