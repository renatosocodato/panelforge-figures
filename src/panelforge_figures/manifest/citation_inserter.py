def render_suggestions_markdown(result: CitationInsertionResult) -> str:
    """Render a human-readable Markdown report.

    Sections:

    * Headline summary (counts).
    * One block per suggestion (sentence excerpt, cite keys,
      confidence, rationale).
    * BibTeX preview footer listing the keys that would be added.

    Safe to call on an empty result — emits a short "no suggestions"
    note.
    """
    lines: list[str] = []
    lines.append("# Citation Suggestions")
    lines.append("")
    lines.append(f"_Manuscript_: `{result.manuscript_path}`")
    lines.append("")
    lines.append(
        f"_Sentences scanned_: **{result.n_sentences_scanned}**, "
        f"suggestions: **{result.n_suggestions}**, "
        f"applied: **{result.n_applied}**, "
        f"new bib entries: **{len(result.new_bib_entries)}**"
    )
    lines.append("")

    if result.backup_manuscript_path is not None:
        lines.append(f"Backup manuscript: `{result.backup_manuscript_path}`")
    if result.backup_bib_path is not None:
        lines.append(f"Backup bib: `{result.backup_bib_path}`")
    if result.backup_manuscript_path or result.backup_bib_path:
        lines.append("")

    if not result.suggestions:
        lines.append("_No suggestions to apply._")
        lines.append("")
        return "\n".join(lines).rstrip() + "\n"

    lines.append("## Suggestions")
    lines.append("")
    for i, sug in enumerate(result.suggestions, start=1):
        # Truncate very long sentences for readable preview.
        sent_preview = sug.sentence.strip()
        if len(sent_preview) > 200:
            sent_preview = sent_preview[:197] + "..."
        lines.append(f"### {i}. line {sug.line_number}")
        lines.append("")
        lines.append(f"> {sent_preview}")
        lines.append("")
        lines.append(f"- **cite keys**: `{', '.join(sug.cite_keys)}`")
        lines.append(f"- **confidence**: {sug.confidence:.2f}")
        lines.append(f"- **rationale**: {sug.rationale}")
        lines.append("")

    if result.new_bib_entries:
        lines.append("## New BibTeX entries")
        lines.append("")
        lines.append("```bibtex")
        for entry in result.new_bib_entries:
            lines.append(entry.to_bibtex().rstrip())
        lines.append("```")
        lines.append("")

    return "\n".join(lines).rstrip() + "\n"
