_STOPWORDS: frozenset[str] = frozenset({
    "a", "an", "and", "as", "at", "be", "by", "for", "from", "has", "in",
    "is", "it", "its", "of", "on", "or", "that", "the", "this", "to",
    "via", "with", "are", "we", "our", "their", "these", "those", "but",
    "if", "into", "than", "when", "where", "which", "while", "who",
    "how", "why", "what", "such",
})

_CITE_KEY_INVALID = re.compile(r"[^a-z0-9]+")
_SURNAME_PUNCT = re.compile(r"[^A-Za-z\-]")


_INITIAL_TOKEN_RE = re.compile(r"^[A-Z](?:\.[A-Z])*\.?$")


def _normalise_surname(author: str) -> str:
    """Extract a single-word surname from a free-form author string.

    Supported author formats:

    * ``"Doe, John"`` — comma-separated surname-first → "doe".
    * ``"Doe J."`` / ``"Doe J"`` — Consensus / Vancouver style
      (surname then initials) → "doe".
    * ``"J. Doe"`` / ``"John A. Doe"`` — Western order (initials
      then surname) → "doe".

    Returns a lowercase BibTeX-safe token (alpha only, hyphens
    preserved). Falls back to ``"anon"`` when the input is unusable.
    """
    if not author:
        return "anon"
    # "Doe, J." → "Doe"
    if "," in author:
        token = author.split(",", 1)[0]
    else:
        tokens = [t for t in author.split() if t]
        if not tokens:
            return "anon"
        # Heuristic: if the LAST token looks like initials (one or
        # more capital letters separated/followed by '.'), assume
        # Consensus / Vancouver order ("Doe J.") and use the FIRST
        # token. Otherwise use the LAST token ("John A. Doe").
        if len(tokens) >= 2 and _INITIAL_TOKEN_RE.match(tokens[-1]):
            token = tokens[0]
        else:
            token = tokens[-1]
    cleaned = _SURNAME_PUNCT.sub("", token).lower()
    return cleaned or "anon"


def _significant_word(title: str) -> str:
    """Pick the first non-stopword token from ``title`` for the cite key.

    Returns lowercase alpha-only; falls back to ``"work"`` when no
    suitable token exists.
    """
    if not title:
        return "work"
    tokens = re.findall(r"[A-Za-z]+", title.lower())
    for tok in tokens:
        if tok not in _STOPWORDS and len(tok) >= 3:
            return tok
    return tokens[0] if tokens else "work"


def _generate_cite_key(
    paper: dict[str, Any],
    *,
    existing_keys: set[str],
) -> str:
    """Generate a unique ``surnameYEARtopic`` cite key.

    Disambiguates collisions by appending ``a``, ``b``, ``c``, ... —
    same convention BibTeX itself uses for same-author-same-year
    duplicates.
    """
    authors = paper.get("authors") or []
    first_author = ""
    if authors:
        first_author = authors[0] if isinstance(authors[0], str) else ""
    surname = _normalise_surname(first_author)

    year = paper.get("year")
    if isinstance(year, int) and year > 0:
        year_str = str(year)
    elif isinstance(year, str):
        m = re.search(r"\d{4}", year)
        year_str = m.group(0) if m else "nd"
    else:
        year_str = "nd"

    topic = _significant_word(paper.get("title", ""))
    base = f"{surname}{year_str}{topic}"
    base = _CITE_KEY_INVALID.sub("", base.lower())
    if not base:
        base = "ref"

    if base not in existing_keys:
        return base
    # Collision: append a, b, c, ...
    for suffix_int in range(26):
        suffix = chr(ord("a") + suffix_int)
        candidate = f"{base}{suffix}"
        if candidate not in existing_keys:
            return candidate
    # Pathological: append a numeric index
    i = 2
    while f"{base}_{i}" in existing_keys:
        i += 1
    return f"{base}_{i}"


def _bibtex_escape(value: str) -> str:
    """Escape a value for safe insertion in a BibTeX ``{ ... }`` field.

    * Strips leading/trailing whitespace and collapses runs of
      whitespace to a single space.
    * Replaces unescaped ``&`` with ``\\&`` (BibTeX expects backslash-
      escapes; raw ``&`` triggers ``unbalanced @`` errors in many
      bibtex flavours).
    * Leaves Unicode intact — modern bibtex / biblatex handle UTF-8.
    """
    if not value:
        return ""
    cleaned = re.sub(r"\s+", " ", str(value)).strip()
    cleaned = re.sub(r"(?<!\\)&", r"\\&", cleaned)
    return cleaned


def _format_authors(authors: list[Any]) -> str:
    """Render an author list in BibTeX ``First Last and First Last`` form.

    Accepts strings (already formatted) or anything ``str()`` can
    coerce. Empty input → empty string (caller drops the field).
    """
    if not authors:
        return ""
    cleaned = [str(a).strip() for a in authors if a]
    return " and ".join(cleaned)


def build_bib_entries_from_consensus(
    papers: list[dict[str, Any]],
    *,
    existing_keys: set[str] | None = None,
) -> tuple[BibEntry, ...]:
    """Convert raw Consensus paper records to :class:`BibEntry` tuples.

    Parameters
    ----------
    papers
        List of paper dicts (as cached by E9). Tolerant of missing
        fields — only ``title`` is treated as load-bearing; everything
        else is filled tolerantly.
    existing_keys
        Cite keys already present in the project's
        ``references.bib``. Newly-generated keys are guaranteed
        unique against this set.

    Returns
    -------
    tuple[BibEntry, ...]
        Entries in input order, with collision-disambiguated keys.
    """
    existing = set(existing_keys or ())
    entries: list[BibEntry] = []
    for paper in papers:
        title = paper.get("title", "").strip()
        if not title:
            # Papers without titles are useless — skip silently.
            continue
        cite_key = _generate_cite_key(paper, existing_keys=existing)
        existing.add(cite_key)

        fields: dict[str, str] = {}
        fields["title"] = _bibtex_escape(title)
        authors = _format_authors(paper.get("authors") or [])
        if authors:
            fields["author"] = _bibtex_escape(authors)
        journal = paper.get("journal", "")
        if journal:
            fields["journal"] = _bibtex_escape(journal)
        year = paper.get("year")
        if isinstance(year, int) and year > 0:
            fields["year"] = str(year)
        elif isinstance(year, str):
            m = re.search(r"\d{4}", year)
            if m:
                fields["year"] = m.group(0)
        doi = paper.get("doi")
        if doi:
            fields["doi"] = _bibtex_escape(str(doi))
        abstract = paper.get("abstract") or paper.get("abstract_excerpt")
        if abstract:
            # Cap abstract length to keep .bib lean; full abstracts
            # bloat the file and stress BibTeX line-length limits.
            short = _bibtex_escape(str(abstract))[:600]
            if short:
                fields["abstract"] = short

        entries.append(
            BibEntry(entry_type="article", cite_key=cite_key, fields=fields)
        )
    return tuple(entries)


