class InserterError(RuntimeError):
    """Raised when the inserter cannot proceed (unreadable manuscript,
    write failure, malformed cache layout).
    """


@dataclass(frozen=True)
class CitationSuggestion:
    """One suggested ``\\cite{...}`` insertion.

    Attributes
    ----------
    sentence
        The full claim sentence as it appears in the manuscript (with
        any inline LaTeX commands left verbatim — we do not rewrite the
        prose).
    line_number
        1-indexed line in the manuscript where the sentence terminates.
        Multi-line sentences resolve to their *last* line so the
        in-place writer touches a single line.
    char_offset
        0-indexed column on ``line_number`` at which the citation
        marker should be inserted. The writer inserts
        ``" \\cite{key1,key2}"`` *before* the terminal punctuation
        (so ``"X is true."`` becomes ``"X is true \\cite{...}."``).
    cite_keys
        The BibTeX keys to cite — usually the top-3 papers from the
        matching Consensus query.
    confidence
        TF-IDF cosine similarity between the cached query and the
        claim sentence, clamped to ``[0, 1]``.
    rationale
        Human-readable explanation surfaced in the dry-run report.
    """

    sentence: str
    line_number: int
    char_offset: int
    cite_keys: tuple[str, ...]
    confidence: float
    rationale: str


@dataclass(frozen=True)
class BibEntry:
    """A BibTeX entry derived from a Consensus paper record.

    Attributes
    ----------
    entry_type
        BibTeX entry type — almost always ``"article"`` for Consensus
        hits.
    cite_key
        Canonical ``surnameYEARtopic`` identifier, generated by
        :func:`_generate_cite_key`. Always BibTeX-safe (no spaces,
        punctuation, or Unicode).
    fields
        Field map (``title``, ``author``, ``journal``, ``year``,
        ``doi``, optionally ``url``, ``abstract``). All values
        BibTeX-escaped (braces around capital words, ``\\&`` for
        ampersands).
    """

    entry_type: str
    cite_key: str
    fields: dict[str, str]

    def to_bibtex(self) -> str:
        """Render canonical BibTeX entry.

        Field order is fixed: ``title``, ``author``, ``journal``,
        ``year``, ``doi``, then any remaining fields alphabetically.
        Trailing-comma format so future fields can be appended cleanly.
        """
        ordered_keys = ("title", "author", "journal", "year", "doi")
        emitted: list[str] = []
        seen: set[str] = set()
        for k in ordered_keys:
            if k in self.fields and self.fields[k]:
                emitted.append(f"  {k} = {{{self.fields[k]}}}")
                seen.add(k)
        for k in sorted(self.fields):
            if k in seen:
                continue
            v = self.fields[k]
            if v:
                emitted.append(f"  {k} = {{{v}}}")
        body = ",\n".join(emitted)
        return f"@{self.entry_type}{{{self.cite_key},\n{body}\n}}\n"


@dataclass(frozen=True)
class CitationInsertionResult:
    """Aggregate result of one ``suggest_citations_for_manuscript`` call.

    Holds the dry-run preview (suggestions + bib entries) and the
    on-disk artefacts produced when ``apply_citation_insertions`` is
    chained on top.
    """

    manuscript_path: Path
    n_sentences_scanned: int
    n_suggestions: int
    n_applied: int
    new_bib_entries: tuple[BibEntry, ...]
    suggestions: tuple[CitationSuggestion, ...]
    backup_manuscript_path: Path | None
    backup_bib_path: Path | None


