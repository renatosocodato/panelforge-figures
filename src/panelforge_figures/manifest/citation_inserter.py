_BIB_KEY_RE = re.compile(
    r"@(?:article|book|inproceedings|incollection|misc|techreport|"
    r"phdthesis|mastersthesis|unpublished|manual|booklet|proceedings|"
    r"online|electronic|software|dataset)\s*\{\s*([^,\s]+)\s*,",
    re.IGNORECASE,
)


def _extract_existing_cite_keys(bib_path: Path) -> set[str]:
    """Parse a ``.bib`` file just enough to collect existing cite keys.

    The parser is deliberately shallow — it only finds the key after
    each ``@entry{`` marker. Returns an empty set when the path does
    not exist or cannot be read.
    """
    if not bib_path.exists() or not bib_path.is_file():
        return set()
    try:
        text = bib_path.read_text(encoding="utf-8")
    except (OSError, UnicodeDecodeError):
        return set()
    return {m.group(1).strip() for m in _BIB_KEY_RE.finditer(text)}


# --------------------------------------------------------------------------- #
# TF-IDF similarity (lightweight, no external deps)                           #
# --------------------------------------------------------------------------- #


_WORD_RE = re.compile(r"\b[a-z][a-z]+\b")


def _tokenise(text: str) -> list[str]:
    """Lowercase alphabetic-only tokenizer (matches manuscript_alignment)."""
    return _WORD_RE.findall(text.lower())


def _term_freq(tokens: list[str]) -> dict[str, float]:
    if not tokens:
        return {}
    counts: dict[str, int] = {}
    for t in tokens:
        counts[t] = counts.get(t, 0) + 1
    n = len(tokens)
    return {t: c / n for t, c in counts.items()}


def _doc_freq(docs: list[list[str]]) -> dict[str, int]:
    df: dict[str, int] = {}
    for doc in docs:
        for t in set(doc):
            df[t] = df.get(t, 0) + 1
    return df


def _idf(df: dict[str, int], n_docs: int) -> dict[str, float]:
    return {t: math.log((n_docs + 1) / (c + 1)) + 1.0 for t, c in df.items()}


def _tfidf_vec(tokens: list[str], idf: dict[str, float]) -> dict[str, float]:
    tf = _term_freq(tokens)
    return {t: f * idf.get(t, 1.0) for t, f in tf.items()}


def _cosine(a: dict[str, float], b: dict[str, float]) -> float:
    common = set(a) & set(b)
    if not common:
        return 0.0
    dot = sum(a[t] * b[t] for t in common)
    na = math.sqrt(sum(v * v for v in a.values()))
    nb = math.sqrt(sum(v * v for v in b.values()))
    if na == 0.0 or nb == 0.0:
        return 0.0
    return dot / (na * nb)


def _clamp01(x: float) -> float:
    return min(1.0, max(0.0, x))


# --------------------------------------------------------------------------- #
# Insertion-site detection                                                    #
# --------------------------------------------------------------------------- #


def _existing_cites_in_sentence(sentence: str) -> bool:
    """Return ``True`` if the sentence already contains a ``\\cite``/``[@..]``.

    The inserter refuses to double-cite — if the user has already
    backed the sentence with at least one citation, we leave it
    untouched.
    """
    if re.search(r"\\cite[a-zA-Z]*\s*\{", sentence):
        return True
    return bool(re.search(r"\[(?:-?@[^@\];\]]+)(?:;\s*-?@[^@\];\]]+)*\]", sentence))


def _find_insertion_offset(line: str) -> int:
    """Return the column at which to splice ``\\cite{...}`` into ``line``.

    Prefers the index *just before* the terminal punctuation (so
    ``"X is true."`` becomes ``"X is true \\cite{...}."``). Falls back
    to the end of the rstripped line when no terminal punctuation is
    present.
    """
    rstripped = line.rstrip()
    if not rstripped:
        return len(line)
    last = rstripped[-1]
    if last in ".!?":
        return len(rstripped) - 1
    if last in '"”’\'':
        # Trailing quote — look one char back.
        if len(rstripped) >= 2 and rstripped[-2] in ".!?":
            return len(rstripped) - 2
    return len(rstripped)


# --------------------------------------------------------------------------- #
# End-to-end pipeline                                                         #
# --------------------------------------------------------------------------- #


def _build_idf_for_pair(
    queries: list[str], claims: list[str],
) -> tuple[dict[str, float], dict[str, list[str]]]:
    """Compute a shared IDF across queries + claim sentences.

    Returns ``(idf, tokens_by_doc)`` where ``tokens_by_doc`` maps each
    distinct doc string to its token list (so callers can re-use the
    vector built from the IDF without re-tokenising).
    """
    tokens_by_doc: dict[str, list[str]] = {}
    for s in queries + claims:
        if s not in tokens_by_doc:
            tokens_by_doc[s] = _tokenise(s)
    df = _doc_freq(list(tokens_by_doc.values()))
    idf = _idf(df, max(len(tokens_by_doc), 1))
    return idf, tokens_by_doc


def _best_query_for_claim(
    claim_sentence: str,
    cache: dict[str, list[dict[str, Any]]],
    *,
    idf: dict[str, float],
    tokens_by_doc: dict[str, list[str]],
) -> tuple[str | None, float]:
    """Find the cached query whose text best matches ``claim_sentence``.

    Returns ``(query, similarity)``; ``(None, 0.0)`` when the cache
    is empty.
    """
    if not cache:
        return None, 0.0
    claim_vec = _tfidf_vec(tokens_by_doc[claim_sentence], idf)
    best_query: str | None = None
    best_sim = 0.0
    for query in cache:
        q_vec = _tfidf_vec(tokens_by_doc[query], idf)
        sim = _cosine(claim_vec, q_vec)
        if sim > best_sim:
            best_sim = sim
            best_query = query
    return best_query, _clamp01(best_sim)


def suggest_citations_for_manuscript(
    manuscript_path: Path,
    *,
    cache_dir: Path | None = None,
    min_similarity: float = 0.6,
    existing_bib_path: Path | None = None,
    top_n_papers: int = 3,
) -> CitationInsertionResult:
    """End-to-end dry-run pipeline: scan, match, emit suggestions.

    1. ``parse_manuscript`` → :class:`ExistingManuscript` (E10).
    2. ``scan_consensus_cache`` → ``{query → papers}``.
    3. Compute IDF over (cached queries) ∪ (claim sentences).
    4. For each claim sentence, score against every cached query;
       pick the best match.
    5. Above ``min_similarity``: take the top-N papers, build
       :class:`BibEntry` objects with unique cite keys, emit a
       :class:`CitationSuggestion`.

    Dry-run: nothing is written. ``n_applied == 0`` and both backup
    paths are ``None`` in the returned result.

    Parameters
    ----------
    manuscript_path
        Path to a ``.tex`` or ``.md`` manuscript.
    cache_dir
        Path to the Consensus cache directory. Defaults to
        ``manuscript_path.parent / "panelforge_workspace/.consensus_cache"``.
    min_similarity
        TF-IDF cosine threshold below which a claim sentence is left
        un-cited.
    existing_bib_path
        Optional ``references.bib`` to dedup against. When provided,
        generated cite keys never collide with the existing file.
    top_n_papers
        How many papers from the best-matching cached query to cite.

    Raises
    ------
    InserterError
        On parse failures or unreadable manuscript.
    """
    # Lazy-import the parser so test isolation stays clean.
    from panelforge_figures.manifest.manuscript_parse import (
        ManuscriptParseError,
        parse_manuscript,
    )

    try:
        manuscript = parse_manuscript(manuscript_path)
    except ManuscriptParseError as exc:
        raise InserterError(f"cannot parse manuscript: {exc}") from exc

    cache_path = cache_dir or (manuscript_path.parent / CACHE_DIR_DEFAULT)
    cache = scan_consensus_cache(cache_path)

    existing_keys = _extract_existing_cite_keys(existing_bib_path) if existing_bib_path else set()

    # The claim sentences from E10's parser already classify each
    # sentence that touches a figure. We treat *every* such sentence
    # as a candidate insertion site, deduplicating by (line_number,
    # sentence) so the same sentence containing two figure refs only
    # gets cited once.
    seen_claims: set[tuple[int, str]] = set()
    unique_claims: list[tuple[int, str]] = []
    for c in manuscript.claims:
        key = (c.line_number, c.sentence)
        if key in seen_claims:
            continue
        seen_claims.add(key)
        unique_claims.append(key)

    n_scanned = len(unique_claims)

    # Build shared IDF across cached queries + claim sentences so the
    # cosine similarity is calibrated against the actual corpus.
    queries = list(cache.keys())
    claim_strings = [s for _, s in unique_claims]
    if not queries or not claim_strings:
        return CitationInsertionResult(
            manuscript_path=manuscript_path,
            n_sentences_scanned=n_scanned,
            n_suggestions=0,
            n_applied=0,
            new_bib_entries=(),
            suggestions=(),
            backup_manuscript_path=None,
            backup_bib_path=None,
        )

    idf, tokens_by_doc = _build_idf_for_pair(queries, claim_strings)

    # Re-read the manuscript text so we can resolve sentence-end
    # offsets accurately. (The parser doesn't preserve the raw lines.)
    try:
        raw_text = manuscript_path.read_text(encoding="utf-8")
    except (OSError, UnicodeDecodeError) as exc:
        raise InserterError(f"cannot re-read manuscript: {exc}") from exc
    lines = raw_text.splitlines()

    suggestions: list[CitationSuggestion] = []
    new_entries: list[BibEntry] = []
    suggested_keys: set[str] = set(existing_keys)

    for line_no, sentence in unique_claims:
        if _existing_cites_in_sentence(sentence):
            continue

        best_query, sim = _best_query_for_claim(
            sentence, cache, idf=idf, tokens_by_doc=tokens_by_doc,
        )
        if best_query is None or sim < min_similarity:
            continue

        papers = cache[best_query][:top_n_papers]
        new_for_query = build_bib_entries_from_consensus(
            papers, existing_keys=suggested_keys,
        )
        if not new_for_query:
            continue
        for e in new_for_query:
            suggested_keys.add(e.cite_key)
        new_entries.extend(new_for_query)

        # Resolve the insertion offset on the manuscript line.
        idx = line_no - 1
        if 0 <= idx < len(lines):
            offset = _find_insertion_offset(lines[idx])
        else:
            offset = len(sentence)

        rationale = (
            f"matched cached query ‘{best_query}’ "
            f"(cosine {sim:.2f}); top-{len(new_for_query)} papers from cache"
        )
        suggestions.append(
            CitationSuggestion(
                sentence=sentence,
                line_number=line_no,
                char_offset=offset,
                cite_keys=tuple(e.cite_key for e in new_for_query),
                confidence=sim,
                rationale=rationale,
            )
        )

    return CitationInsertionResult(
        manuscript_path=manuscript_path,
        n_sentences_scanned=n_scanned,
        n_suggestions=len(suggestions),
        n_applied=0,
        new_bib_entries=tuple(new_entries),
        suggestions=tuple(suggestions),
        backup_manuscript_path=None,
        backup_bib_path=None,
    )


